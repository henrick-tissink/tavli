/** Canonical uuid shape check, shared by API input validation and client
 * guards (mock fixtures use integer ids that must never reach uuid columns). */
export const UUID_RE =
  /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/i;

export function isUuid(value: string): boolean {
  return UUID_RE.test(value);
}
